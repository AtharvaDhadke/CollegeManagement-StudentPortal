    void User_Verification(String username , String password)
    {
        try
        {
             Class.forName("com.mysql.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/universitydb","AtharvaDhadke","Atharva");
           
            String sql ="select * from  studentdb where username = ? and Password = ?";
            
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1,username);
            pst.setString(2,password);
            ResultSet rs=pst.executeQuery();
            if(rs.next())
            {
            Student_Dashboard home = new Student_Dashboard();
            home.show();
            this.dispose();
            }
            
            else
            {
                JOptionPane.showMessageDialog(this , "Wrong Username and Password");
            }
        }
    
        catch(Exception e)
        {
            
        }
    }

