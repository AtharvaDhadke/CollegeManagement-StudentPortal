    private void lbl_adminLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_adminLoginMouseClicked
        // TODO add your handling code here:
        
        
        adusername = tf_AdminUsername.getText();
        adpassword = tf_AdminPassword.getText();
         
         if(adusername.trim().equals("") || adpassword.trim().equals(""))
         {
             lbl_adminerror.setText("Please Enter Username and Password!");
         }
         else
         {
             Admin_Verification(adusername , adpassword);
             
         }
    }//GEN-LAST:event_lbl_adminLoginMouseClicked

